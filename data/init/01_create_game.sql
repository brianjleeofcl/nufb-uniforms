CREATE TABLE game (
    id INTEGER GENERATED ALWAYS AS IDENTITY,
    season INTEGER NOT NULL,
    attendance INTEGER,
    betting_line VARCHAR,
    broadcast VARCHAR,
    canonical_week INTEGER NOT NULL,
    capacity INTEGER,
    city VARCHAR,
    game_date TIMESTAMP WITH TIME ZONE,
    final BOOLEAN NOT NULL,
    canceled BOOLEAN NOT NULL,
    game_espnid VARCHAR NOT NULL UNIQUE,
    grass_field BOOLEAN,
    home BOOLEAN NOT NULL,
    opponent VARCHAR NOT NULL,
    opponent_abbrev VARCHAR,
    opponent_espnid VARCHAR,
    opponent_mascot VARCHAR,
    opponent_ranking INTEGER,
    opponent_score INTEGER,
    opponent_score1_q INTEGER,
    opponent_score2_q INTEGER,
    opponent_score3_q INTEGER,
    opponent_score4_q INTEGER,
    opponent_score_ot INTEGER,
    opponent_season_losses INTEGER,
    opponent_season_wins INTEGER,
    opponent_stats JSON,
    overtime INTEGER,
    ranking INTEGER,
    regular_season BOOLEAN NOT NULL,
    score INTEGER,
    score1_q INTEGER,
    score2_q INTEGER,
    score3_q INTEGER,
    score4_q INTEGER,
    score_ot INTEGER,
    season_losses INTEGER,
    season_wins INTEGER,
    stadium VARCHAR,
    stats JSON,
    title VARCHAR,
    win BOOLEAN,
    zip VARCHAR,
    UNIQUE(canonical_week, season, regular_season)
);
